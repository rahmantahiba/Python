#Default shape commands 

import turtle

t = turtle.Turtle()

#Changing turtle tile command

turtle.title("Circle Program")

#Changing color of the turtle 

t.fillcolor("red")

#Chaging the pencolor 

t.pencolor("green")

#Changing color of both

t.color("green", "red")

t.circle(60)

#Creating a dot 

t.dot(20)

#Command for background color but color can also be changed using hex code

turtle.bgcolor("Blue")

#Changing turtle shape

t.shape("turtle")
t.shape("arrow")
t.shape("circle")
t.shape("square")
t.shape("triangle")

